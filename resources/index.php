<p><a href="/Category:Minecraft_Classic" title="Category:Minecraft Classic"><img alt="Banner classic.png" src="/images/8/80/Banner_classic.png" width="800" height="125" /></a>
</p><p>Minecraft Classic is the original version of Minecraft, available for free to the public. It's still available off of the main website today and is still played by many people, with an active development community. As it's been around since 2010, and because it's so simplistic, there are many, many programs made to work with Classic - there are over <b>18+</b> servers alone, written in everything from C++ to PureBasic to Perl. It's highly recommend if you're planning to do a development project at this point to instead look into the Minecraft Modern specifications.
</p>
<h3> <span class="mw-headline" id="Documentation_2"> Documentation </span></h3>
