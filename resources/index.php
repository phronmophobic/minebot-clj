<p><a href="/Category:Minecraft_Modern" title="Category:Minecraft Modern"><img alt="Banner beta.png" src="/images/3/3e/Banner_beta.png" width="800" height="125" /></a>
</p><p>Minecraft Modern is the latest (and only updated) version of Minecraft which requires a paid account to download, update and play. Most of the new development projects focus on either creating entirely new programs from scratch that interoperate with Minecraft (such as a bot or server) or modding projects that wrap the client or server and provide bug fixes, new features and enhancements to existing features.
</p>
<h3> <span class="mw-headline" id="Documentation"> Documentation </span></h3>
<p>There are ongoing efforts to keep reverse engineered documentation updated, but it isn't as easy as it sounds. The protocol generally changes with each release, and both the Client and Server classes get rearranged on each release. Below are links to the current documentation segments, which <b>may or may not be up to date</b>.
